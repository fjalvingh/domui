	private final String m_css;
	private final String m_prefix;

	FaIcon(String css, String prefix) {
		m_css = css;
		m_prefix = prefix;
	}

	public String getCssClassName() {
		return m_prefix + " " + m_css;
	}

	@Override
	public String getClasses() {
		return m_prefix + " " + m_css;
	}

	@Override public NodeBase createNode(String cssClasses) {
		return new FontIcon(this).css(cssClasses);
	}

	public IFontIconRef css(String... classes) {
		return new WrappedIconRef(this, classes);
	}

	public IFontIconRef solid() {
		return withPrefix("fas");
	}

	public IFontIconRef regular() {
		return withPrefix("far");
	}

	public IFontIconRef withPrefix(String prefix) {
		if(m_prefix.equals(prefix)) {
			return this;
		}
		return new FaIcon(m_css, prefix);

	}

	public String getPrefix() {
		return m_prefix;
	}

	public String getCss() {
		return m_css;
	}

